let year = prompt("Будь-ласка, введіть рік вашого народження", 1991);
if (isNaN(year) || year.trim() === "") {
    console.log("Щось ви ввели не те, спробуйте ще раз");
} else {
    let age = 2024 - year;
    if (age > 0) {
        let lastDigit = age % 10; 
        let lastTwoDigits = age % 100; //якщо вік менше 100 років, то поверне просто вік, а не остачу, що теж підходить
        
        if (lastDigit === 1 && lastTwoDigits !== 11) {
            console.log("Вам " + age + " рік");
        } else if ((lastDigit >= 2 && lastDigit <= 4) && !(lastTwoDigits >= 12 && lastTwoDigits <= 14)) {
            console.log("Вам " + age + " роки");
        } else {
            console.log("Вам " + age + " років");
        }
    } else {
        console.log("Щось ви ввели не те, спробуйте ще раз");
    }
}

